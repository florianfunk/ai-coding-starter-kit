-- Entfernt nicht mehr benötigte Textfelder aus produkte:
-- - datenblatt_text_2, datenblatt_text_3 (UI auf einen Block reduziert)
-- - treiber                              (Feld aus Formular entfernt)
-- - bild_detail_1_text/2_text/3_text     (Detail-Texte entfernt)
--
-- v_produkt_listing referenziert diese Spalten — daher View droppen,
-- Spalten entfernen und View ohne die Spalten neu anlegen.

DROP VIEW IF EXISTS public.v_produkt_listing;

ALTER TABLE public.produkte
  DROP COLUMN IF EXISTS datenblatt_text_2,
  DROP COLUMN IF EXISTS datenblatt_text_3,
  DROP COLUMN IF EXISTS treiber,
  DROP COLUMN IF EXISTS bild_detail_1_text,
  DROP COLUMN IF EXISTS bild_detail_2_text,
  DROP COLUMN IF EXISTS bild_detail_3_text;

CREATE VIEW public.v_produkt_listing AS
SELECT
  p.id,
  p.external_id,
  p.artikelnummer,
  p.bereich_id,
  p.kategorie_id,
  p.name,
  p.sortierung,
  p.artikel_bearbeitet,
  p.hauptbild_path,
  p.datenblatt_titel,
  p.datenblatt_text,
  p.leistung_w,
  p.nennstrom_a,
  p.nennspannung_v,
  p.schutzklasse,
  p.spannungsart,
  p.gesamteffizienz_lm_w,
  p.lichtstrom_lm,
  p.abstrahlwinkel_grad,
  p.energieeffizienzklasse,
  p.farbtemperatur_k,
  p.farbkonsistenz_sdcm,
  p.farbwiedergabeindex_cri,
  p.led_chip,
  p.lichtverteilung,
  p.ugr,
  p.masse_text,
  p.laenge_mm,
  p.breite_mm,
  p.hoehe_mm,
  p.aussendurchmesser_mm,
  p.einbaudurchmesser_mm,
  p.gewicht_g,
  p.gehaeusefarbe,
  p.montageart,
  p.schlagfestigkeit,
  p.schutzart_ip,
  p.werkstoff_gehaeuse,
  p.leuchtmittel,
  p.sockel,
  p.rollenlaenge_m,
  p.maximale_laenge_m,
  p.anzahl_led_pro_meter,
  p.abstand_led_zu_led_mm,
  p.laenge_abschnitte_mm,
  p.kleinster_biegeradius_mm,
  p.lebensdauer_h,
  p.temperatur_ta,
