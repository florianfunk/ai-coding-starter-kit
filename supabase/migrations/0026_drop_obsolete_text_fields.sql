  p.info_kurz,
  p.datenblatt_art,
  p.marker_info,
  p.marker_1,
  p.marker_2,
  p.marker_3,
  p.marker_preis_status,
  p.nicht_leer,
  p.sortierung_fest,
  p.sortierung_alt,
  p.sortierung_ber,
  p.kat_startseite,
  p.bild_detail_1_path,
  p.bild_detail_2_path,
  p.bild_zeichnung_1_path,
